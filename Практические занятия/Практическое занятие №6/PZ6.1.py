# 1. Дан список A размера N и целое число K (1 < K < N). 
# Преобразовать список, увеличив каждый его элемент на исходное значение элемента AK.

def increase_by_k_element(a, k):
    #Увеличивает каждый элемент списка a на значение элемента a[k-1].
    try:
        ak = a[k - 1]  # Индексация начинается с 0, поэтому k-1
        return [x + ak for x in a]
    except IndexError:
        print("Ошибка: индекс K выходит за границы списка.")
        return a

# Пример вызова
a = [1, 2, 3, 4, 5]
k = 3
result = increase_by_k_element(a, k)
print(f"Исходный список: {a}")
print(f"Преобразованный список: {result}")