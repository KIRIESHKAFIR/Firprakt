# Описать функцию Swap(X, Y), меняющую содержимое переменных Х и У 
# (Х и Ү - вещественные параметры, являющиеся одновременно входными и выходными).
# С ее помощью для данных переменных А, В, С, D последовательно поменять содержимое следующих пар: 
# А и В, С и D, В и С и вывести новые значения А, В, С, D.

def swap(x, y):
    return y, x

def swap_variables():
    try:
        # Ввод переменных
        a = float(input("Введите значение A: "))
        b = float(input("Введите значение B: "))
        c = float(input("Введите значение C: "))
        d = float(input("Введите значение D: "))

        # Меняем значения
        a, b = swap(a, b)
        c, d = swap(c, d)
        b, c = swap(b, c)

        print(f"Новые значения: A = {a}, B = {b}, C = {c}, D = {d}")
        return a, b, c, d
    except ValueError:
        print("Ошибка: введите вещественные числа.")
        return None

# Пример вызова
swap_variables()